export type AskonConfig = 'basic' | 'cowls' | 'cowls_nozzle';

export interface AskonVent {
  A: number;              // cm
  B: number;              // cm
  Av: number;             // m² = A*B/10000
  Aa_basic: number;       // m² — bez owiewek
  Aa_cowls: number;       // m² — z owiewkami
  Aa_cowls_nozzle: number; // m² — z owiewkami i dyszą
  current: 4 | 6;         // A — pobór prądu siłownika
}

export function askonAa(v: AskonVent, cfg: AskonConfig): number {
  if (cfg === 'cowls') return v.Aa_cowls;
  if (cfg === 'cowls_nozzle') return v.Aa_cowls_nozzle;
  return v.Aa_basic;
}

function e(A: number, B: number, b: number, c: number, cn: number, amp: 4 | 6): AskonVent {
  return { A, B, Av: A * B / 10000, Aa_basic: b, Aa_cowls: c, Aa_cowls_nozzle: cn, current: amp };
}

