export type AwakConfig = 'basic' | 'cowls' | 'cowls_nozzle';

export interface AwakVent {
  A: number;              // cm
  B: number;              // cm
  Av: number;             // m² = A*B/10000
  Aa_basic: number;       // m²
  Aa_cowls: number;       // m²
  Aa_cowls_nozzle: number; // m²
}

export function awakAa(v: AwakVent, cfg: AwakConfig): number {
  if (cfg === 'cowls') return v.Aa_cowls;
  if (cfg === 'cowls_nozzle') return v.Aa_cowls_nozzle;
  return v.Aa_basic;
}

function e(A: number, B: number, b: number, c: number, cn: number): AwakVent {
  return { A, B, Av: A * B / 10000, Aa_basic: b, Aa_cowls: c, Aa_cowls_nozzle: cn };
}

